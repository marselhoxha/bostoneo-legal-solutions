package com.bostoneo.bostoneosolutions.enums;

/**
 * Categories for different types of permissions to control access levels
 */
public enum PermissionCategory {
    BASIC,          // Basic permissions available to most users
    ADMINISTRATIVE, // Administrative permissions for managers
    FINANCIAL,      // Financial permissions for billing/accounting
    CONFIDENTIAL,   // Confidential permissions for sensitive data
    SYSTEM          // System-level permissions for administrators
} 